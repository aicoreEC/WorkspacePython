#!/usr/bin/env python3
'''
  셔뱅( shebang ) : 현재 파일을 실행해 줄 프로그램을 지정할 때 사용한다.
                    셔뱅에 파이썬 경로를 지정해 놓으면 해당 파이썬으로 .py파일을 실행
'''
print( 'Hello, World!!!' )

# 파이썬 숫자 : 정수( integer ), 실수( floating-point ), 복소수( complex number )
# 사칙연산
print()
print( 1 + 1 )
print( 1 - 2 )
print( 2 * 2 )
print( 5 / 2 )      # 나눗셈 결과 : 실수
print( 5 // 2 )     # 나눗셈 결과 : 정수, 버림 나눗셈( floor division ), 소수점 이하는 버림
                    # 실수에 사용하면 결과는 실수가 나오며 소수점 이하는 버림, 결과는 항상 .0으로 끝남
print()
print( 5.5 // 2 )
print( 4 // 2.0 )
print( 4.1 // 2.1 )

print()
print( 5 % 2 )      # 나머지( modulo )

print()
print( 2 ** 10 )    # 거듭제곱, 숫자를 특정 횟수만큼 곱한다.

